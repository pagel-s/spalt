Eigen::Isometry3d Molecule::align_to_molecule(Molecule& target_mol,
                                              unsigned int target_conformer_id,
                                              unsigned int source_conformer_id) {
    // Check if conformers exist
    if (mol == nullptr || mol->getNumConformers() <= source_conformer_id) {
        throw std::runtime_error("Source conformer " + std::to_string(source_conformer_id) +
                                 " does not exist");
    }
    if (target_mol.mol == nullptr || target_mol.mol->getNumConformers() <= target_conformer_id) {
        throw std::runtime_error("Target conformer " + std::to_string(target_conformer_id) +
                                 " does not exist");
    }

    // Check if surfaces exist
    auto source_surface_it = surface_cache_.find(source_conformer_id);
    if (source_surface_it == surface_cache_.end()) {
        throw std::runtime_error("Source surface for conformer " +
                                 std::to_string(source_conformer_id) + " not found");
    }

    auto target_surface_it = target_mol.surface_cache_.find(target_conformer_id);
    if (target_surface_it == target_mol.surface_cache_.end()) {
        throw std::runtime_error("Target surface for conformer " +
                                 std::to_string(target_conformer_id) + " not found");
    }

    // Get surfaces
    std::shared_ptr<Surface> source_surface = source_surface_it->second;
    std::shared_ptr<Surface> target_surface = target_surface_it->second;

    // ESP properties should be computed before calling alignment
    // Alignment method only uses already computed properties for coloring

    // Create Open3D point clouds with colors
    auto source_cloud = std::make_shared<open3d::geometry::PointCloud>();
    auto target_cloud = std::make_shared<open3d::geometry::PointCloud>();

    // Convert source surface to Open3D point cloud with existing normals
    for (const auto& vertex_pair : source_surface->vertices) {
        const auto& coords = vertex_pair.second.second;
        if (coords.size() >= 3) {
            source_cloud->points_.emplace_back(coords[0], coords[1], coords[2]);
            // Use existing normals if available (elements 3-5), otherwise add empty normal
            if (coords.size() >= 6) {
                source_cloud->normals_.emplace_back(coords[3], coords[4], coords[5]);
            } else {
                source_cloud->normals_.emplace_back(0.0, 0.0, 0.0);
            }
        }
    }

    // Convert target surface to Open3D point cloud with existing normals
    for (const auto& vertex_pair : target_surface->vertices) {
        const auto& coords = vertex_pair.second.second;
        if (coords.size() >= 3) {
            target_cloud->points_.emplace_back(coords[0], coords[1], coords[2]);
            // Use existing normals if available (elements 3-5), otherwise add empty normal
            if (coords.size() >= 6) {
                target_cloud->normals_.emplace_back(coords[3], coords[4], coords[5]);
            } else {
                target_cloud->normals_.emplace_back(0.0, 0.0, 0.0);
            }
        }
    }

    // Add colors based on ALL computed properties for better alignment
    bool has_source_colors = false;
    bool has_target_colors = false;

    // Helper function to get property values from cache - completely dynamic
    auto getPropertyValues =
        [](const std::map<std::string, std::unordered_map<std::string, std::any>>& cache,
           unsigned int conformer_id, const std::string& property_name) -> std::vector<double> {
        std::string cache_key = std::to_string(conformer_id) + "_" + property_name;
        auto cache_it = cache.find(cache_key);
        if (cache_it != cache.end()) {
            // Try common value key patterns dynamically
            std::vector<std::string> possible_keys = {
                property_name + "_values",     // e.g., "esp_values"
                "electrostatic_potential",     // ESP specific
                property_name + "_potential",  // e.g., "hbond_potential"
                property_name + "_charges",    // e.g., "esp_charges"
                property_name                  // Direct property name
            };

            for (const auto& key : possible_keys) {
                auto values_it = cache_it->second.find(key);
                if (values_it != cache_it->second.end()) {
                    try {
                        return std::any_cast<std::vector<double>>(values_it->second);
                    } catch (const std::exception&) {
                        // Try next key
                        continue;
                    }
                }
            }
        }
        return {};
    };

    // Helper function to normalize property value to [-1, 1] range using PropertyParamsRegistry
    auto normalizePropertyValue = [](const std::string& property_name, double value) -> double {
        try {
            // Use PropertyParamsRegistry for dynamic property parameters
            PropertyParams params = PropertyParamsRegistry::getParams(property_name);
            double min_val = params.min_value;
            double max_val = params.max_value;

            if (max_val > min_val) {
                double normalized = (value - min_val) / (max_val - min_val);
                normalized = std::max(0.0, std::min(1.0, normalized));  // Clamp to [0, 1]
                return 2.0 * normalized - 1.0;                          // Convert to [-1, 1]
            }
        } catch (const std::exception&) {
            // Property not found in registry, use default range
            double min_val = -1.0, max_val = 1.0;
            double normalized = (value - min_val) / (max_val - min_val);
            normalized = std::max(0.0, std::min(1.0, normalized));
            return 2.0 * normalized - 1.0;
        }
        return 0.0;
    };

    // Get all available properties from source - completely dynamic
    std::vector<std::string> available_properties;
    std::string conformer_prefix = std::to_string(source_conformer_id) + "_";

    for (const auto& [key, _] : surface_property_cache_) {
        if (key.find(conformer_prefix) == 0) {
            std::string property_name = key.substr(conformer_prefix.length());

            // Check if this property has actual values (not just min/max)
            auto values =
                getPropertyValues(surface_property_cache_, source_conformer_id, property_name);
            if (!values.empty()) {
                available_properties.push_back(property_name);
            }
        }
    }

    if (!available_properties.empty()) {
        source_cloud->colors_.clear();
        source_cloud->colors_.reserve(source_cloud->points_.size());

        for (size_t i = 0; i < source_cloud->points_.size(); ++i) {
            Eigen::Vector3d combined_color(0.0, 0.0, 0.0);
            int valid_properties = 0;

            for (const auto& property : available_properties) {
                auto values =
                    getPropertyValues(surface_property_cache_, source_conformer_id, property);
                if (i < values.size()) {
                    double normalized_value = normalizePropertyValue(property, values[i]);

                    // Dynamic color mapping based on property name patterns
                    // This creates a unique color signature for each property type
                    std::hash<std::string> hasher;
                    size_t hash = hasher(property);

                    // Use hash to create consistent color components for each property
                    double r_factor = ((hash & 0xFF) / 255.0) * 0.5 + 0.25;  // 0.25 to 0.75
                    double g_factor = (((hash >> 8) & 0xFF) / 255.0) * 0.5 + 0.25;
                    double b_factor = (((hash >> 16) & 0xFF) / 255.0) * 0.5 + 0.25;

                    // Add property contribution to combined color
                    combined_color[0] += r_factor * (1.0 + normalized_value) / 2.0;
                    combined_color[1] += g_factor * (1.0 + normalized_value) / 2.0;
                    combined_color[2] += b_factor * (1.0 + normalized_value) / 2.0;

                    valid_properties++;
                }
            }

            if (valid_properties > 0) {
                combined_color /= valid_properties;  // Average the colors
                // Ensure colors are in valid range
                combined_color = combined_color.cwiseMax(Eigen::Vector3d::Zero())
                                     .cwiseMin(Eigen::Vector3d::Ones());
                source_cloud->colors_.push_back(combined_color);
            } else {
                source_cloud->colors_.emplace_back(0.5, 0.5, 0.5);  // Gray fallback
            }
        }
        has_source_colors = true;
    }

    if (!available_properties.empty()) {
        target_cloud->colors_.clear();
        target_cloud->colors_.reserve(target_cloud->points_.size());

        for (size_t i = 0; i < target_cloud->points_.size(); ++i) {
            Eigen::Vector3d combined_color(0.0, 0.0, 0.0);
            int valid_properties = 0;

            for (const auto& property : available_properties) {
                auto values = getPropertyValues(target_mol.surface_property_cache_,
                                                target_conformer_id, property);
                if (i < values.size()) {
                    double normalized_value = normalizePropertyValue(property, values[i]);

                    // Dynamic color mapping based on property name patterns
                    // This creates a unique color signature for each property type
                    std::hash<std::string> hasher;
                    size_t hash = hasher(property);

                    // Use hash to create consistent color components for each property
                    double r_factor = ((hash & 0xFF) / 255.0) * 0.5 + 0.25;  // 0.25 to 0.75
                    double g_factor = (((hash >> 8) & 0xFF) / 255.0) * 0.5 + 0.25;
                    double b_factor = (((hash >> 16) & 0xFF) / 255.0) * 0.5 + 0.25;

                    // Add property contribution to combined color
                    combined_color[0] += r_factor * (1.0 + normalized_value) / 2.0;
                    combined_color[1] += g_factor * (1.0 + normalized_value) / 2.0;
                    combined_color[2] += b_factor * (1.0 + normalized_value) / 2.0;

                    valid_properties++;
                }
            }

            if (valid_properties > 0) {
                combined_color /= valid_properties;  // Average the colors
                // Ensure colors are in valid range
                combined_color = combined_color.cwiseMax(Eigen::Vector3d::Zero())
                                     .cwiseMin(Eigen::Vector3d::Ones());
                target_cloud->colors_.push_back(combined_color);
            } else {
                target_cloud->colors_.emplace_back(0.5, 0.5, 0.5);  // Gray fallback
            }
        }
        has_target_colors = true;
    }

    // Set uniform colors if no property colors are available
    if (!has_source_colors) {
        source_cloud->colors_.resize(source_cloud->points_.size(),
                                     Eigen::Vector3d(0.5, 0.5, 0.5));  // Gray
    }
    if (!has_target_colors) {
        target_cloud->colors_.resize(target_cloud->points_.size(),
                                     Eigen::Vector3d(0.5, 0.5, 0.5));  // Gray
    }

    // Set up ICP parameters
    double voxel_size = 1.0;  // Angstroms
    auto source_down = source_cloud->VoxelDownSample(voxel_size);
    auto target_down = target_cloud->VoxelDownSample(voxel_size);

    // Ensure normals are available for registration
    // Strategy: Use existing normals from surface vertices when available,
    //           otherwise fall back to Open3D normal estimation
    if (source_down->normals_.empty()) {
        source_down->EstimateNormals();
    }
    if (target_down->normals_.empty()) {
        target_down->EstimateNormals();
    }

    open3d::pipelines::registration::RegistrationResult result;

    // Prioritize colored ICP if properties are available
    if (has_source_colors && has_target_colors && !available_properties.empty()) {
        try {
#ifdef DEBUG
            std::cout << "Attempting colored ICP registration with " << available_properties.size()
                      << " properties..." << std::endl;
#endif
            result = open3d::pipelines::registration::RegistrationColoredICP(
                *source_down, *target_down, voxel_size, Eigen::Matrix4d::Identity(),
                open3d::pipelines::registration::TransformationEstimationForColoredICP(),
                open3d::pipelines::registration::ICPConvergenceCriteria(1e-6, 1e-6, 50));
#ifdef DEBUG
            std::cout << "✓ Used colored ICP registration with property-based colors (fitness: "
                      << result.fitness_ << ")" << std::endl;
#endif
        } catch (const std::exception& e) {
            std::cerr << "Warning: Colored ICP failed, falling back to standard ICP: " << e.what()
                      << std::endl;
            result = open3d::pipelines::registration::RegistrationICP(
                *source_down, *target_down, voxel_size * 2.0, Eigen::Matrix4d::Identity(),
                open3d::pipelines::registration::TransformationEstimationPointToPoint(),
                open3d::pipelines::registration::ICPConvergenceCriteria(1e-6, 1e-6, 30));
#ifdef DEBUG
            std::cout << "Used standard ICP registration (fitness: " << result.fitness_ << ")"
                      << std::endl;
#endif
        }
    } else {
// Fallback to standard ICP
#ifdef DEBUG
        std::cout << "No properties available, using standard ICP registration..." << std::endl;
#endif
        result = open3d::pipelines::registration::RegistrationICP(
            *source_down, *target_down, voxel_size * 2.0, Eigen::Matrix4d::Identity(),
            open3d::pipelines::registration::TransformationEstimationPointToPoint(),
            open3d::pipelines::registration::ICPConvergenceCriteria(1e-6, 1e-6, 30));
#ifdef DEBUG
        std::cout << "Used standard ICP registration (fitness: " << result.fitness_ << ")"
                  << std::endl;
#endif
    }

#ifdef DEBUG
    std::cout << "Registration completed!" << std::endl;
    std::cout << "Fitness: " << result.fitness_ << std::endl;
    std::cout << "Inlier RMSE: " << result.inlier_rmse_ << std::endl;
#endif
    // Convert Open3D matrix to Eigen Isometry3d
    Eigen::Isometry3d transformation;
    transformation.matrix() = result.transformation_;

    return transformation;
}

