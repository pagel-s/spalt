std::vector<std::unique_ptr<Molecule>> Molecule::loadFromMultiMolFile(const std::string& filename) {
    std::vector<std::unique_ptr<Molecule>> molecules;

    std::string ext = getFileExtension(filename);
    if (ext != "sdf") {
        throw std::runtime_error("Multi-molecule loading only supported for SDF files");
    }

    try {
        RDKit::SDMolSupplier supplier(filename);

        while (!supplier.atEnd()) {
            auto romol_ptr = std::unique_ptr<RDKit::ROMol>(supplier.next());
            if (romol_ptr) {
                std::string temp_smiles =
                    "Loaded from " + filename + "_mol_" + std::to_string(molecules.size() + 1);
                auto temp_mol = std::make_unique<Molecule>(temp_smiles.c_str());
                temp_mol->mol = std::make_unique<RDKit::RWMol>(*romol_ptr);

                temp_mol->get_charge_from_mol();

                // Move the unique_ptr into the vector - NO raw pointers!
                molecules.push_back(std::move(temp_mol));
            }
        }

        if (molecules.empty()) {
            throw std::runtime_error("No molecules found in file: " + filename);
        }

    } catch (const std::exception& e) {
        throw std::runtime_error("Error loading molecules from " + filename + ": " + e.what());
    }

    return molecules;
}

std::string Molecule::getFileExtension(const std::string& filename) {
    size_t dot_pos = filename.find_last_of('.');
    if (dot_pos == std::string::npos) {
        throw std::runtime_error("No file extension found in: " + filename);
    }

    std::string ext = filename.substr(dot_pos + 1);
    std::transform(ext.begin(), ext.end(), ext.begin(), ::tolower);
    return ext;
}

void Molecule::compute(const std::string& property_name, unsigned int conformer_id,
                       SurfaceParams surface_params) {
    // Check if property is registered
    auto registry_it = property_registry_.find(property_name);
    if (registry_it == property_registry_.end()) {
        throw std::runtime_error("Property '" + property_name +
                                 "' is not registered. Available properties: " + [this]() {
                                     std::string available;
                                     for (const auto& pair : property_registry_) {
                                         if (!available.empty())
                                             available += ", ";
                                         available += pair.first;
                                     }
                                     return available.empty() ? "none" : available;
                                 }());
    }


    int surface_id =
        createSurface(conformer_id, surface_params.num_vertices, surface_params.radius,
                      surface_params.density, surface_params.hdensity, surface_params.type,
                      surface_params.program, surface_params.sample_method, surface_params.addH);

    // Get the surface object
    auto surface_it = surface_cache_.find(surface_id);
    if (surface_it == surface_cache_.end()) {
        throw std::runtime_error("Surface not found after creation");
    }

    std::shared_ptr<Surface> surface = surface_it->second;

    // Create property object using the factory
    std::unique_ptr<ISurfaceProperty> property;

    // print property name and smiles
    auto curr_smiles = RDKit::MolToSmiles(*mol);
    // Special handling for ESP property to use the charge method from surface_params
    if (property_name == "esp") {
        property = std::make_unique<SurfaceESPProperty>(surface_params.charge_method);
    } else {
        property = registry_it->second();
    }

    // Create property cache
    std::unordered_map<std::string, std::any> cache;

    // Compute the property
    property->compute(*surface, cache);

    // Store the cache using surface_id + property_name as key
    surface_property_cache_[std::to_string(surface_id) + "_" + property_name] = cache;
}

void Molecule::registerProperty(const std::string& property_name,
                                std::function<std::unique_ptr<ISurfaceProperty>()> factory) {
    property_registry_[property_name] = factory;
}

void Molecule::generate3DCoordinates() {
    if (mol == nullptr)
        return;

    // Check if molecule already has 3D coordinates
    bool has_3d = false;
    if (mol->getNumConformers() > 0) {
        const auto& conf = mol->getConformer();
        if (conf.is3D()) {
            has_3d = true;
        }
    }

    // Generate 3D coordinates if not present
    if (!has_3d) {
        generateSingleConformer();
    }
}

void Molecule::generateSingleConformer() {
    if (mol == nullptr)
        return;

    try {
        int cid = RDKit::DGeomHelpers::EmbedMolecule(*mol);

        if (cid >= 0) {
            try {
                RDKit::MMFF::MMFFOptimizeMolecule(*mol, cid);
            } catch (const std::exception&) {
                try {
                    RDKit::UFF::UFFOptimizeMolecule(*mol, cid);
                } catch (const std::exception& e) {
                    std::cerr << "Warning: Force field optimization failed for single conformer: "
                              << e.what() << std::endl;
                }
            }
        } else {
            std::cerr << "Error: Failed to generate conformer using RDKit" << std::endl;
        }

    } catch (const std::exception& e) {
        std::cerr << "Error generating single conformer: " << e.what() << std::endl;
    }
}

