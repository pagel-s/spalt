Molecule::Molecule(const char* smiles) {
    this->smiles = smiles;
    this->save_smiles_to_mol();
    this->get_charge_from_mol();

    // Note: Conformers are generated by the caller (CLI) as needed
    // This allows for better control over conformer generation parameters

    registerDefaultProperties();
}

Molecule::Molecule(const std::string& filename) {
    std::ifstream file(filename);
    if (!file.is_open()) {
        throw std::runtime_error("Cannot open file: " + filename);
    }

    std::string ext = getFileExtension(filename);

    try {
        if (ext == "sdf") {
            // filename, sanitize: true, removeHs: false
            RDKit::SDMolSupplier supplier(filename, true, false);
            if (!supplier.atEnd()) {
                auto romol_ptr = std::unique_ptr<RDKit::ROMol>(supplier.next());
                if (romol_ptr) {
                    mol = std::make_unique<RDKit::RWMol>(*romol_ptr);
                } else {
                    throw std::runtime_error("Failed to parse molecule from SDF file: " + filename);
                }
            } else {
                throw std::runtime_error("No molecules found in SDF file: " + filename);
            }
        } else {
            throw std::runtime_error("Unsupported file format: " + ext +
                                     ". Only SDF files are currently supported.");
        }

        if (!mol) {
            throw std::runtime_error("Failed to parse molecule from file: " + filename);
        }

        smiles = RDKit::MolToSmiles(*mol);

        get_charge_from_mol();

        bool has_valid_3d = false;
        if (mol->getNumConformers() > 0) {
            const RDKit::Conformer& conf = mol->getConformer(0);
            if (conf.is3D()) {
                has_valid_3d = true;
            }
        }

        if (mol->getNumConformers() == 0 || !has_valid_3d) {
            if (mol->getNumConformers() > 0) {
                mol->clearConformers();
            }
            calculate_conformers(1);
        }

    } catch (const std::exception& e) {
        throw std::runtime_error("Error loading molecule from " + filename + ": " + e.what());
    }

    registerDefaultProperties();
}

Molecule::~Molecule() {
}

Molecule::Molecule(Molecule&& other) noexcept
    : smiles(std::move(other.smiles)), charge(other.charge), mol(std::move(other.mol)),
      surface_cache_(std::move(other.surface_cache_)),
      surface_property_cache_(std::move(other.surface_property_cache_)),
      property_registry_(std::move(other.property_registry_)) {
    // Reset the moved-from object
    other.charge = 0;
}

Molecule& Molecule::operator=(Molecule&& other) noexcept {
    if (this != &other) {
        smiles = std::move(other.smiles);
        charge = other.charge;
        mol = std::move(other.mol);
        surface_cache_ = std::move(other.surface_cache_);
        surface_property_cache_ = std::move(other.surface_property_cache_);
        property_registry_ = std::move(other.property_registry_);

        // Reset the moved-from object
        other.charge = 0;
    }
    return *this;
}

