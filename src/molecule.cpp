bool Molecule::mol_to_molfile(std::string_view filename) const {
    if (this->mol == nullptr) {
        std::cerr << "Error: Cannot write molfile, molecule is null" << std::endl;
        return false;
    }
    try {
        MolToMolFile(*this->mol, std::string(filename));
        return true;
    } catch (const std::exception& e) {
        std::cerr << "Error writing molfile: " << e.what() << std::endl;
        return false;
    }
}

void Molecule::calculate_conformers(unsigned int num_conformers) const {
    if (this->mol == nullptr) {
        std::cout << "Cannot calculate conformers: molecule is null" << std::endl;
        return;
    }
    try {
        RDKit::INT_VECT mol_cids = RDKit::DGeomHelpers::EmbedMultipleConfs(
            *this->mol, static_cast<unsigned int>(num_conformers));

        for (unsigned int cid : mol_cids) {
            try {
                RDKit::MMFF::MMFFOptimizeMolecule(*this->mol, cid);
            } catch (const std::exception&) {
                try {
                    RDKit::UFF::UFFOptimizeMolecule(*this->mol, cid);
                } catch (const std::exception& e) {
                    std::cerr << "Warning: Force field optimization failed for conformer " << cid
                              << ": " << e.what() << std::endl;
                }
            }
        }

    } catch (const std::exception& e) {
        std::cerr << "Error generating conformers: " << e.what() << std::endl;
    }
}

std::unordered_map<int, int> Molecule::generateAdvancedConformers(const ConformerParams& params) {
    if (this->mol == nullptr) {
        std::cerr << "Cannot generate advanced conformers: molecule is null" << std::endl;
        return {};
    }

    try {
        ConformerGenerator generator(params);
        // Convert unique_ptr to shared_ptr for the conformer generator
        std::shared_ptr<RDKit::ROMol> shared_mol(this->mol.get(), [](RDKit::ROMol*) {});
        auto cluster_map = generator.generateConformers(shared_mol);

        // Remove unwanted conformers and keep only the selected ones
        if (!cluster_map.empty()) {
            // Create a new molecule with only the selected conformers
            auto new_mol = std::make_unique<RDKit::RWMol>(*this->mol);
            new_mol->clearConformers();

            // Add only the selected conformers
            for (const auto& [cluster_id, conformer_id] : cluster_map) {
                // Safety check: ensure conformer_id is valid
                if (conformer_id < 0 || static_cast<unsigned int>(conformer_id) >= this->mol->getNumConformers()) {
                    std::cerr << "Warning: Invalid conformer ID " << conformer_id 
                              << " (total conformers: " << this->mol->getNumConformers() 
                              << "), skipping cluster " << cluster_id << std::endl;
                    continue;
                }
                
                auto selected_conf =
                    std::make_unique<RDKit::Conformer>(this->mol->getConformer(conformer_id));
                // Reset the conformer ID to sequential numbering
                selected_conf->setId(new_mol->getNumConformers());
                new_mol->addConformer(selected_conf.release(),
                                      false);  // false = don't assign new ID
            }

            // Replace the original molecule
            this->mol = std::move(new_mol);
        }

        return cluster_map;
    } catch (const std::exception& e) {
        std::cerr << "Error generating advanced conformers: " << e.what() << std::endl;
        return {};
    }
}

void Molecule::save_smiles_to_mol() {
    try {
        std::unique_ptr<RDKit::RWMol> mol(RDKit::SmilesToMol(smiles));
        if (mol == nullptr) {
            throw std::runtime_error("Error parsing SMILES string");
        }
        this->mol = std::move(mol);
    } catch (const std::exception& e) {
        std::cerr << "Error saving smiles to mol: " << e.what() << std::endl;
        this->mol = nullptr;
    }
}

void Molecule::get_charge_from_mol() {
    if (this->mol == nullptr) {
        return;
    }

    this->charge = RDKit::MolOps::getFormalCharge(*this->mol);
}

void Molecule::add_hydrogens_to_mol() {
    if (this->mol == nullptr) {
        return;
    }
    RDKit::MolOps::addHs(*this->mol);
}

void Molecule::add_hydrogens_to_mol_if_needed() {
    if (this->mol == nullptr) {
        return;
    }
    RDKit::MolOps::addHs(*this->mol, false, true);
}

void Molecule::remove_hydrogens_from_mol() {
    if (this->mol == nullptr) {
        return;
    }
    RDKit::MolOps::removeHs(*this->mol);
}

int Molecule::createSurface(unsigned int conformer_id, unsigned int num_vertices, double radius,
                            double density, double hdensity, std::string type, std::string program,
                            std::string sample_method, bool addH) {
    // Check if conformer exists
    if (mol == nullptr || mol->getNumConformers() <= conformer_id) {
        throw std::runtime_error("Conformer " + std::to_string(conformer_id) + " does not exist");
    }

    auto it = surface_cache_.find(conformer_id);
    if (it != surface_cache_.end()) {
        return conformer_id;  // Surface already exists
    }

    if (addH) {
        add_hydrogens_to_mol_if_needed();
    } else {
        remove_hydrogens_from_mol();
    }

    // smiles
    std::string curr_smiles = RDKit::MolToSmiles(*mol);

    auto surface = std::make_shared<Surface>(*this, conformer_id, num_vertices, radius, density,
                                             hdensity, type, program, sample_method);

    if (!addH) {
        add_hydrogens_to_mol_if_needed();
    }

    surface_cache_[conformer_id] = surface;
    return conformer_id;
}

const std::map<int, std::pair<std::string, std::vector<double>>>& Molecule::getSurfaceVertices(
    unsigned int conformer_id) const {
    auto it = surface_cache_.find(conformer_id);
    if (it == surface_cache_.end()) {
        throw std::runtime_error("Surface not found for conformer " + std::to_string(conformer_id));
    }
    return it->second->vertices;
}

void Molecule::clearSurfaceCache() {
    surface_cache_.clear();
}

unsigned int Molecule::getSurfaceVertexCount(unsigned int conformer_id) const {
    auto it = surface_cache_.find(conformer_id);
    if (it == surface_cache_.end()) {
        throw std::runtime_error("Surface not found for conformer " + std::to_string(conformer_id));
    }
    return static_cast<int>(it->second->vertices.size());
}

std::string Molecule::generateSurfaceCacheKey(unsigned int num_vertices, double radius,
                                              double density, double hdensity,
                                              const std::string& type,
                                              const std::string& program) const {
    std::ostringstream oss;
    oss << num_vertices << "_" << radius << "_" << density << "_" << hdensity << "_" << type << "_"
        << program;
    return oss.str();
}

int Molecule::getNumConformers() const {
    if (mol == nullptr) {
        return 0;
    }
    return mol->getNumConformers();
}

std::vector<int> Molecule::getConformerIds() const {
    std::vector<int> ids;
    if (mol == nullptr) {
        return ids;
    }

    unsigned int num_conformers = mol->getNumConformers();
    for (unsigned int i = 0; i < num_conformers; ++i) {
        ids.push_back(i);
    }
    return ids;
}

bool Molecule::transformConformer(unsigned int conformer_id,
                                  const Eigen::Isometry3d& transformation) {
    if (mol == nullptr || mol->getNumConformers() <= conformer_id) {
        throw std::runtime_error("Conformer " + std::to_string(conformer_id) + " does not exist");
    }

    try {
        auto& conf = mol->getConformer(conformer_id);
        int num_atoms = conf.getNumAtoms();

        // Apply transformation to each atom's coordinates
        for (int i = 0; i < num_atoms; ++i) {
            auto pos = conf.getAtomPos(i);
            Eigen::Vector3d coord(pos.x, pos.y, pos.z);

            // Apply transformation (homogeneous coordinates)
            Eigen::Vector3d transformed = transformation * coord;

            // Update the conformer coordinates
            conf.setAtomPos(i, RDGeom::Point3D(transformed.x(), transformed.y(), transformed.z()));
        }

        // Invalidate cached surface for this conformer since coordinates changed
        auto it = surface_cache_.find(conformer_id);
        if (it != surface_cache_.end()) {
            surface_cache_.erase(it);
        }

        return true;
    } catch (const std::exception& e) {
        std::cerr << "Error transforming conformer " << conformer_id << ": " << e.what()
                  << std::endl;
        return false;
    }
}

bool Molecule::transformSurface(unsigned int conformer_id,
                                const Eigen::Isometry3d& transformation) {
    // Check if surface exists for this conformer
    auto it = surface_cache_.find(conformer_id);
    if (it == surface_cache_.end()) {
        throw std::runtime_error("Surface not found for conformer " + std::to_string(conformer_id));
    }

    try {
        // Apply transformation directly to surface vertices
        it->second->transformSurface(transformation);
        return true;
    } catch (const std::exception& e) {
        std::cerr << "Error transforming surface for conformer " << conformer_id << ": " << e.what()
                  << std::endl;
        return false;
    }
}

void Molecule::saveConformerSDF(unsigned int conformer_id, const std::string& filename,
                                bool include_properties) const {
    if (mol == nullptr || mol->getNumConformers() <= conformer_id) {
        throw std::runtime_error("Conformer " + std::to_string(conformer_id) + " does not exist");
    }

    try {
        auto mol_copy = std::make_unique<RDKit::RWMol>(*mol);

        std::vector<int> conformer_ids;
        for (unsigned int i = 0; i < mol_copy->getNumConformers(); ++i) {
            conformer_ids.push_back(i);
        }

        for (unsigned int cid : conformer_ids) {
            if (cid != conformer_id) {
                mol_copy->removeConformer(cid);
            }
        }

        mol_copy->setProp("_Name", "Conformer_" + std::to_string(conformer_id));

        MolToMolFile(*mol_copy, filename);

        if (include_properties) {
            std::ofstream file(filename, std::ios::app);
            if (file.is_open()) {
                file << "> <SMILES>\n";
                file << smiles << "\n\n";
                file << "> <FormalCharge>\n";
                file << get_charge() << "\n\n";
                file << "> <ConformerID>\n";
                file << conformer_id << "\n\n";
                file << "> <NumAtoms>\n";
                file << mol_copy->getNumAtoms() << "\n\n";
                file << "$$$$\n";  // SDF end marker
                file.close();
            }
        }
    } catch (const std::exception& e) {
        throw std::runtime_error("Error saving conformer " + std::to_string(conformer_id) + ": " +
                                 e.what());
    }
}

void Molecule::saveMultiConformerSDF(
    const std::vector<std::pair<int, AlignmentResult>>& conformer_results,
    const std::string& filename, bool include_properties) const {
    if (mol == nullptr) {
        throw std::runtime_error("Molecule is null");
    }

    try {
        std::ofstream file(filename);
        if (!file.is_open()) {
            throw std::runtime_error("Cannot open file for writing: " + filename);
        }

        for (const auto& [conformer_id, alignment_result] : conformer_results) {
            if (conformer_id >= static_cast<int>(mol->getNumConformers())) {
                std::cerr << "Warning: Conformer " << conformer_id << " does not exist, skipping..."
                          << std::endl;
                continue;
            }

            // Create a copy of the molecule with only this conformer
            auto mol_copy = std::make_unique<RDKit::RWMol>(*mol);

            // Remove all conformers except the one we want
            std::vector<int> conformer_ids_to_remove;
            for (unsigned int i = 0; i < mol_copy->getNumConformers(); ++i) {
                if (static_cast<int>(i) != conformer_id) {
                    conformer_ids_to_remove.push_back(i);
                }
            }

            // Remove conformers in reverse order to maintain indices
            for (auto it = conformer_ids_to_remove.rbegin(); it != conformer_ids_to_remove.rend();
                 ++it) {
                mol_copy->removeConformer(*it);
            }

            // Set molecule name
            mol_copy->setProp("_Name", "Conformer_" + std::to_string(conformer_id));

            // Write the conformer to file
            std::string mol_block = MolToMolBlock(*mol_copy);
            file << mol_block;

            if (include_properties) {
                // Add alignment scores as SDF properties
                file << "> <SMILES>\n";
                file << smiles << "\n\n";
                file << "> <FormalCharge>\n";
                file << get_charge() << "\n\n";
                file << "> <ConformerID>\n";
                file << conformer_id << "\n\n";
                file << "> <NumAtoms>\n";
                file << mol_copy->getNumAtoms() << "\n\n";
                file << "> <AlignmentFitness>\n";
                file << std::fixed << std::setprecision(6) << alignment_result.fitness << "\n\n";
                file << "> <AlignmentRMSE>\n";
                file << std::fixed << std::setprecision(6) << alignment_result.inlier_rmse
                     << "\n\n";
                file << "> <UsedColoredICP>\n";
                file << (alignment_result.used_colored_icp ? "true" : "false") << "\n\n";

                // Add transformation matrix as properties
                const auto& matrix = alignment_result.transformation.matrix();
                file << "> <TransformationMatrix>\n";
                for (int i = 0; i < 4; ++i) {
                    for (int j = 0; j < 4; ++j) {
                        if (i > 0 || j > 0)
                            file << " ";
                        file << std::fixed << std::setprecision(6) << matrix(i, j);
                    }
                }
                file << "\n\n";
            }

            file << "$$$$\n";  // SDF end marker
        }

        file.close();

    } catch (const std::exception& e) {
        throw std::runtime_error("Error saving multi-conformer SDF: " + std::string(e.what()));
    }
}

void Molecule::saveSurfacePointCloud(unsigned int conformer_id, const std::string& filename,
                                     bool include_normals, bool include_colors,
                                     const std::vector<std::string>& properties) const {
    auto it = surface_cache_.find(conformer_id);
    if (it == surface_cache_.end()) {
        throw std::runtime_error("Surface not found for conformer " + std::to_string(conformer_id));
    }

    // Get property cache for this surface if available
    // Combine all property caches into a single cache for multiple properties
    std::unordered_map<std::string, std::any> combined_cache;
    if (!properties.empty()) {
        for (const auto& property : properties) {
            std::string cache_key = std::to_string(conformer_id) + "_" + property;
            auto cache_it = surface_property_cache_.find(cache_key);
            if (cache_it != surface_property_cache_.end()) {
                // Merge the individual property cache into combined cache
                for (const auto& [key, value] : cache_it->second) {
                    combined_cache[key] = value;
                }
            }
        }
    }

    const std::unordered_map<std::string, std::any>* property_cache = nullptr;
    if (!combined_cache.empty()) {
        property_cache = &combined_cache;
    }

    it->second->savePointCloud(filename, include_normals, include_colors, properties,
                               property_cache);
}

